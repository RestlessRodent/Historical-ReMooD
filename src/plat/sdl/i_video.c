/* IS_ConvertKey() -- Converts an SDL key to a ReMooD Key */
static uint8_t IS_ConvertKey(const SDLKey a_SKey)
{
	/* Check */
	if (a_SKey >= SDLK_LAST)
		return 0;
		
	return c_SDLToReMooDKey[a_SKey];
}

/* I_GetEvent() -- Gets an event and adds it to the queue */
void I_GetEvent(void)
{
	SDL_Event Event;
	I_EventEx_t ExEvent[4];
	int32_t i, j, a, d, o, dc, oc, dir;
	static bool_t Focused = true;
	static bool_t WarpX = false;
	static bool_t WarpY = false;
	static bool_t DownKeys[SDLK_LAST];
	static uint16_t LastUnic[SDLK_LAST];
	SDLKey Key;
	int32_t x, y;
	static int32_t ox;
	static int32_t oy;
	
	/* Clear Event */
	// Somewhere in SDL land...
		// ==00:00:02:25.812 19764== Conditional jump or move depends on uninitialised value(s)
		// ==00:00:02:25.813 19764==    at 0x50E44E6: ??? (in /usr/lib/libSDL-1.2.so.0.11.3)
		// ==00:00:02:25.813 19764==    by 0x50E4D06: ??? (in /usr/lib/libSDL-1.2.so.0.11.3)
		// ==00:00:02:25.813 19764==    by 0x50E575A: ??? (in /usr/lib/libSDL-1.2.so.0.11.3)
		// ==00:00:02:25.813 19764==    by 0x50B996F: SDL_PumpEvents (in /usr/lib/libSDL-1.2.so.0.11.3)
		// ==00:00:02:25.813 19764==    by 0x50B9E08: SDL_PollEvent (in /usr/lib/libSDL-1.2.so.0.11.3)
		// ==00:00:02:25.813 19764==    by 0x4AC2B3: I_GetEvent (i_video.c:661)
	memset(&Event, 0, sizeof(Event));
	
	/* Constantly read events */
	while (SDL_PollEvent(&Event))
	{
		// Clear event
		memset(&ExEvent, 0, sizeof(ExEvent));
		
		// Which event is this now?
		switch (Event.type)
		{
				// Quit Event
			case SDL_QUIT:
				ExEvent[0].Type = IET_QUIT;
				break;
				
				// Focus of the window changes
			case SDL_ACTIVEEVENT:
				Focused = ! !Event.active.gain;
				continue;
				
				// Key event
			case SDL_KEYDOWN:
				ExEvent[0].Data.Keyboard.Down = true;
			case SDL_KEYUP:
				Key = Event.key.keysym.sym;
				
				// Keyed data out of range?
				if (Key >= SDLK_LAST)
					continue;
					
				// Repeated event?
				if (ExEvent[0].Data.Keyboard.Down && DownKeys[Key])
					ExEvent[0].Data.Keyboard.Repeat = true;
					
				// Actual data
				ExEvent[0].Type = IET_KEYBOARD;
				ExEvent[0].Data.Keyboard.KeyCode = IS_ConvertKey(Key);
				
				if (ExEvent[0].Data.Keyboard.Down)
					LastUnic[Key] = ExEvent[0].Data.Keyboard.Character = Event.key.keysym.unicode;
				else
				{
					// Remeber unicode key (hack)
					ExEvent[0].Data.Keyboard.Character = LastUnic[Key];
					LastUnic[Key] = 0;
				}
				
				// Set down state
				DownKeys[Key] = ExEvent[0].Data.Keyboard.Down;
				break;
				
				// Mouse Motion:
			case SDL_MOUSEMOTION:
				// SDL has a stack based event system, which means that if the
				// mouse cursor is warped, any old events in the old location
				// still exist. Then on top of that the warping creates a new
				// mouse event. Very fun. So basically, the old x position has
				// to be remembered only when it is set to the old position on
				// a warp, otherwise it is not needed.
				
				// Calculate Screen Center
				i = l_SDLSurface->w >> 1;
				j = l_SDLSurface->h >> 1;
				a = l_SDLSurface->w >> 2;
				d = l_SDLSurface->h >> 2;
				
				// Difference from last mouse location
				// But warping needs to be checked because warps generate new
				// events.
				if (!WarpX || (WarpX && Event.motion.x != i))
				{
					x = ((int32_t)Event.motion.x) - ox;
					ox = Event.motion.x;
				}
				else
				{
					x = 0;
					ox = i;
				}
				
				if (!WarpY || (WarpY && Event.motion.y != j))
				{
					y = ((int32_t)Event.motion.y) - oy;
					oy = Event.motion.y;
					
					// Invert
					y = -y;
				}
				else
				{
					y = 0;
					oy = j;
				}
				
				// Build event for this mouse
				ExEvent[0].Type = IET_MOUSE;
				ExEvent[0].Data.Mouse.MouseID = 0;	// Always mouse Zero
				ExEvent[0].Data.Mouse.Pos[0] = Event.motion.x;
				ExEvent[0].Data.Mouse.Pos[1] = Event.motion.y;
				ExEvent[0].Data.Mouse.Move[0] = x;
				ExEvent[0].Data.Mouse.Move[1] = y;
				
				// Set to current location
				x = Event.motion.x;
				y = Event.motion.y;
				
				// Fall off/y x area? Only if focused and grabbed
				if (Focused && l_DoGrab)
					if ((x < (i - a) || x > (i + a)) ||
						(y < (j - d) || y > (j + d)))
					{
						if (x < (i - a) || x > (i + a))
						{
							x = i;
							WarpX = true;
						}
					
						if (y < (j - d) || y > (j + d))
						{
							y = j;
							WarpY = true;
						}
					
						// Warp
						SDL_WarpMouse(x, y);
					}
				break;
				
				// Mouse button
			case SDL_MOUSEBUTTONDOWN:
				ExEvent[0].Data.Mouse.Down = 1;
			case SDL_MOUSEBUTTONUP:
				ExEvent[0].Type = IET_MOUSE;
				ExEvent[0].Data.Mouse.Button = Event.button.button;
				ExEvent[0].Data.Mouse.Pos[0] = Event.button.x;
				ExEvent[0].Data.Mouse.Pos[1] = Event.button.y;
				break;
				
				// Joystick Axis
			case SDL_JOYAXISMOTION:
				if (Event.jaxis.which >= l_NumJoys || Event.jaxis.axis >= ISJOYMAXMAPS)
					continue;
					
				// Change axis
				ExEvent[0].Type = IET_JOYSTICK;
				ExEvent[0].Data.Joystick.JoyID = Event.jaxis.which;
				ExEvent[0].Data.Joystick.Axis = l_Joys[Event.jaxis.which].AxisMap[Event.jaxis.axis] + 1;
				ExEvent[0].Data.Joystick.Value = Event.jaxis.value;
				break;
				
				// Joystick Ball
			case SDL_JOYBALLMOTION:
				if (Event.jball.which >= l_NumJoys || Event.jball.ball >= ISJOYMAXMAPS)
					continue;
					
				a = 0;
				
				// X axis changed?
				if (Event.jball.xrel != 0)
				{
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jball.which;
					ExEvent[a].Data.Joystick.Axis = l_Joys[Event.jball.which].BallMap[Event.jball.ball][0] + 1;
					ExEvent[a].Data.Joystick.Value = Event.jball.xrel;
					a++;
				}
				// Y axis changed?
				if (Event.jball.yrel != 0)
				{
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jball.which;
					ExEvent[a].Data.Joystick.Axis = l_Joys[Event.jball.which].BallMap[Event.jball.ball][1] + 1;
					ExEvent[a].Data.Joystick.Value = Event.jball.yrel;
				}
				break;
				
				// Joystick Hat
			case SDL_JOYHATMOTION:
				if (Event.jhat.which >= l_NumJoys || Event.jhat.hat >= ISJOYMAXMAPS)
					continue;
					
				a = 0;
				
				// Hat has not changed
				if (l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] == Event.jhat.value)
					continue;
					
				// X Axis Change
				if ((l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] & (SDL_HAT_LEFT | SDL_HAT_RIGHT)) !=
				        (Event.jhat.value & (SDL_HAT_LEFT | SDL_HAT_RIGHT)))
				{
					// Quick direction (old and difference)
					oc = !(l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] & (SDL_HAT_LEFT | SDL_HAT_RIGHT));
					dc = !(Event.jhat.value & (SDL_HAT_LEFT | SDL_HAT_RIGHT));
					o = ((l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] & SDL_HAT_RIGHT) ? 2 : oc);
					d = ((Event.jhat.value & SDL_HAT_RIGHT) ? 2 : dc);
					
					// Release old direction button
					dir = (o == 0 ? 0 : (o == 2 ? 1 : 4));
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jhat.which;
					ExEvent[a].Data.Joystick.Button = l_Joys[Event.jhat.which].HatMapB[Event.jhat.hat][dir] + 1;
					a++;
					
					// Hold new direction button
					dir = (d == 0 ? 0 : (d == 2 ? 1 : 4));
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jhat.which;
					ExEvent[a].Data.Joystick.Down = 1;
					ExEvent[a].Data.Joystick.Button = l_Joys[Event.jhat.which].HatMapB[Event.jhat.hat][dir] + 1;
					a++;
					
					// Reorient X axis
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jhat.which;
					ExEvent[a].Data.Joystick.Axis = l_Joys[Event.jhat.which].HatMapA[Event.jhat.hat][0] + 1;
					ExEvent[a].Data.Joystick.Value = (d == 0 ? -32768 : (d == 2 ? 32767 : 0));
				}
				// Y Axis Change
				if ((l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] & (SDL_HAT_UP | SDL_HAT_DOWN)) != (Event.jhat.value & (SDL_HAT_UP | SDL_HAT_DOWN)))
				{
					// Quick direction (old and difference)
					oc = !(l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] & (SDL_HAT_UP | SDL_HAT_DOWN));
					dc = !(Event.jhat.value & (SDL_HAT_UP | SDL_HAT_DOWN));
					o = ((l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] & SDL_HAT_DOWN) ? 2 : oc);
					d = ((Event.jhat.value & SDL_HAT_DOWN) ? 2 : dc);
					
					// Release old direction button
					dir = (o == 0 ? 2 : (o == 2 ? 3 : 4));
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jhat.which;
					ExEvent[a].Data.Joystick.Button = l_Joys[Event.jhat.which].HatMapB[Event.jhat.hat][dir] + 1;
					a++;
					
					// Hold new direction button
					dir = (d == 0 ? 2 : (d == 2 ? 3 : 4));
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jhat.which;
					ExEvent[a].Data.Joystick.Down = 1;
					ExEvent[a].Data.Joystick.Button = l_Joys[Event.jhat.which].HatMapB[Event.jhat.hat][dir] + 1;
					a++;
					
					// Reorient X axis
					ExEvent[a].Type = IET_JOYSTICK;
					ExEvent[a].Data.Joystick.JoyID = Event.jhat.which;
					ExEvent[a].Data.Joystick.Axis = l_Joys[Event.jhat.which].HatMapA[Event.jhat.hat][1] + 1;
					ExEvent[a].Data.Joystick.Value = (d == 0 ? -32768 : (d == 2 ? 32767 : 0));
				}
				// Set new center
				l_Joys[Event.jhat.which].HatCenter[Event.jhat.hat] = Event.jhat.value;
				break;
				
				// Joystick Button
			case SDL_JOYBUTTONDOWN:
				ExEvent[0].Data.Joystick.Down = 1;
			case SDL_JOYBUTTONUP:
				// Out of range?
				if (Event.jbutton.which >= l_NumJoys || Event.jbutton.button >= ISJOYMAXMAPS)
					continue;
					
				ExEvent[0].Type = IET_JOYSTICK;
				ExEvent[0].Data.Joystick.JoyID = Event.jbutton.which;
				ExEvent[0].Data.Joystick.Button = l_Joys[Event.jbutton.which].ButtonMap[Event.jbutton.button] + 1;
				break;
				
				// Unknown
			default:
				break;
		}
		
		// Send event away
		for (i = 0; i < 4; i++)
			if (ExEvent[i].Type != IET_NULL)
				I_EventExPush(&ExEvent[i]);
	}
}

