		return;
		
//
// wipe update
//
	if (!cv_screenslink.value)
		return;
		
	wipe_EndScreen(0, 0, vid.width, vid.height);
	
	wipestart = I_GetTime() - 1;
	y = wipestart + 2 * TICRATE;	// init a timeout
