/* I_MouseGrab() -- Sets mouse grabbing */
void I_MouseGrab(const bool_t a_Grab)
{
	/* Check */
	if (!l_MouseOn)
		return;
	
	/* Only if changed */
	if (a_Grab == l_DoGrab)
		return;
	
	/* Grabbing? */
	if (a_Grab)
		// Hide the cursor
		SDL_ShowCursor(SDL_DISABLE);
	
	/* Not Grabbing */
	else
		// Show the cursor
		SDL_ShowCursor(SDL_ENABLE);
	
	/* Set new grabbing */
	l_DoGrab = a_Grab;
}

