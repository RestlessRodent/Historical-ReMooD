int main(int argc, char** argv)
{
	FILE* f;
	int i, j;
	uint8_t u;
	
	/* Load skin colors */
	f = fopen("skincols", "rb");
	fread(Skins, 256, 1, f);
	fclose(f);
	
	/* Go through */
	f = fopen("remapped", "wb");
	
	for (i = 0; i < 16; i++)
	{
		for (j = 0; j < 256; j++)
		{
			u = Skins[(16 * i) + (GreenMap[j] - 112)];
			fwrite(&u, 1, 1, f);
		}
	}
	
	fclose(f);
}

