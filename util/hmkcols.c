int main(int argc, char** argv)
{
	FILE* f;
	int i, j;
	uint8_t u;
	
	/* Load skin colors */
	f = fopen("hskincols", "rb");
	fread(Skins, 256, 1, f);
	fclose(f);
	
	/* Go through */
	f = fopen("hremapped", "wb");
	
	for (i = 0; i < 16; i++)
	{
		for (j = 0; j < 256; j++)
		{
			u = Skins[(GreenMap[j] - 225) + (16 * i)];
			fwrite(&u, 1, 1, f);
		}
	}
	
	fclose(f);
}

