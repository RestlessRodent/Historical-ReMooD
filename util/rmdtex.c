/* LoadPPM() -- Loads a PPM */
static Image_t* LoadPPM(FILE* const File, const V_ColorEntry_t* const a_Cols)
{
#define BUFSIZE 512
	Image_t* Create;
	uint8_t Buf[BUFSIZE];
	uint32_t Width, Height;
	uint32_t Depth;
	size_t Tokey, x, y;
	char* Tok;
	V_ColorEntry_t Color;
	
	/* Read header */
	// Check for P6
	ReadPPMToken(File, Buf, BUFSIZE);
	
	if (strcmp(Buf, "P6") != 0)
	{
		fprintf(stderr, "Err: .ppm is not P6 (is \"%s\").\n", Buf);
		return NULL;
	}
	
	/* Constantly read data */
	Tokey = 0;
	while (Tokey < 3)
	{
		// Read another token
		ReadPPMToken(File, Buf, BUFSIZE);
		
		// If it starts with #, skip it (a comment)
		if (Buf[0] == '#')
			continue;
		
		// Image Width
		if (Tokey == 0)
		{
			Width = atoi(Buf);
		}
		
		// Image height
		else if (Tokey == 1)
		{
			Height = atoi(Buf);
		}
		
		// File depth (bits)
		else if (Tokey == 2)
		{
			Depth = atoi(Buf);
		}
		
		// Increment
		Tokey++;
	}
	
	/* Create blank image */
	Create = malloc(sizeof(*Create) + (Width * Height));
	
	if (!Create)
		return NULL;
	
	memset(Create, 0, sizeof(*Create) + (Width * Height));
	Create->Width = Width;
	Create->Height = Height;
	Create->Pixels = ((uint8_t*)Create) + sizeof(*Create);
	
	/* Read image data */
	for (y = 0; y < Height; y++)
		for (x = 0; x < Width; x++)
		{
			// Clear color
			memset(&Color, 0, sizeof(Color));
			
			// Read color
			fread(&Color.RGB.R, 1, 1, File);
			fread(&Color.RGB.G, 1, 1, File);
			fread(&Color.RGB.B, 1, 1, File);
			
			// Low depth?
			Color.RGB.R = (uint8_t)(((double)Color.RGB.R / (double)Depth) * 255.0);
			Color.RGB.G = (uint8_t)(((double)Color.RGB.G / (double)Depth) * 255.0);
			Color.RGB.B = (uint8_t)(((double)Color.RGB.B / (double)Depth) * 255.0);
			
			// Find closest color
			Color = V_RGBtoHSV(Color);
			Create->Pixels[(y * Width) + x] = V_BestRGBMatch(a_Cols, Color, (a_Cols != c_Colors));
		}
	
	/* Return created image */
	return Create;
#undef BUFSIZE
}

