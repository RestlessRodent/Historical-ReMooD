/* V_HSVtoRGB() -- Convert HSV to RGB */
static V_ColorEntry_t V_HSVtoRGB(const V_ColorEntry_t HSV)
{
	int R, G, B, H, S, V, P, Q, T, F;
	V_ColorEntry_t Ret;
	
	/* Get inital values */
	H = HSV.HSV.H;
	S = HSV.HSV.S;
	V = HSV.HSV.V;
	
	/* Gray Color? */
	if (!S)
	{
		R = G = B = V;
	}
	
	/* Real Color */
	else
	{
		// Calculate Hue Shift
		F = ((H % 60) * 255) / 60;
		H /= 60;
		
		// Calculate channel values
		P = (V * (256 - S)) / 256;
		Q = (V * (256 - (S * F) / 256)) / 256;
		T = (V * (256 - (S * (256 - F)) / 256)) / 256;
		
		switch (H)
		{
			case 0:
				R = V;
				G = T;
				B = P;
				break;
			
			case 1:
				R = Q;
				G = V;
				B = P;
				break;
			
			case 2:
				R = P;
				G = V;
				B = T;
				break;
			
			case 3:
				R = P;
				G = Q;
				B = V;
				break;
			
			case 4:
				R = T;
				G = P;
				B = V;
				break;
			
			default:
				R = V;
				G = P;
				B = Q;
				break;
		}
	}
	
	/* Set Return */
	Ret.RGB.R = R;
	Ret.RGB.G = G;
	Ret.RGB.B = B;
	
	/* Return */
	return Ret;
}

/* V_RGBtoHSV() -- Convert RGB to HSV */
static V_ColorEntry_t V_RGBtoHSV(const V_ColorEntry_t RGB)
{
	V_ColorEntry_t Ret;
	uint8_t rMin, rMax, rDif;
	
	// Get min/max
	rMin = 255;
	rMax = 0;

	// Get RGB minimum
	if (RGB.RGB.R < rMin)
		rMin = RGB.RGB.R;
	if (RGB.RGB.G < rMin)
		rMin = RGB.RGB.G;
	if (RGB.RGB.B < rMin)
		rMin = RGB.RGB.B;

	// Get RGB maximum
	if (RGB.RGB.R > rMax)
		rMax = RGB.RGB.R;
	if (RGB.RGB.G > rMax)
		rMax = RGB.RGB.G;
	if (RGB.RGB.B > rMax)
		rMax = RGB.RGB.B;

	// Obtain value
	Ret.HSV.V = rMax;

	// Short circuit?
	if (Ret.HSV.V == 0)
	{
		Ret.HSV.H = Ret.HSV.S = 0;
		return Ret;
	}

	// Obtain difference
	rDif = rMax - rMin;

	// Obtain saturation
	Ret.HSV.S = (uint8_t)(((uint32_t)255 * (uint32_t)rDif) / (uint32_t)Ret.HSV.V);

	// Short circuit?
	if (Ret.HSV.S == 0)
	{
		Ret.HSV.H = 0;
		return Ret;
	}

	/* Obtain hue */
	if (rMax == RGB.RGB.R)
		Ret.HSV.H = 43 * (RGB.RGB.G - RGB.RGB.B) / rMax;
	else if (rMax == RGB.RGB.G)
		Ret.HSV.H = 85 + (43 * (RGB.RGB.B - RGB.RGB.R) / rMax);
	else
		Ret.HSV.H = 171 + (43 * (RGB.RGB.R - RGB.RGB.G) / rMax);
	
	return Ret;
}

/* V_BestHSVMatch() -- Best match between HSV for tables */
static size_t V_BestHSVMatch(const V_ColorEntry_t* const Table, const V_ColorEntry_t HSV)
{
	size_t i, Best;
	V_ColorEntry_t tRGB, iRGB;
	int32_t BestSqr, ThisSqr, Dr, Dg, Db;
	
	/* Check */
	if (!Table)
		return 0;
	
	/* Convert input to RGB */
	iRGB = V_HSVtoRGB(HSV);
	
	/* Loop colors */
	for (Best = 0, BestSqr = 0x7FFFFFFFUL, i = 0; i < 256; i++)
	{
		// Convert table entry to RGB
		tRGB = V_HSVtoRGB(Table[i]);
		
		// Perfect match?
		if (iRGB.RGB.R == tRGB.RGB.R && iRGB.RGB.B == tRGB.RGB.B && iRGB.RGB.G == tRGB.RGB.G)
			return i;
		
		// Distance of colors
		Dr = tRGB.RGB.R - iRGB.RGB.R;
		Dg = tRGB.RGB.G - iRGB.RGB.G;
		Db = tRGB.RGB.B - iRGB.RGB.B;
		ThisSqr = (Dr * Dr) + (Dg * Dg) + (Db * Db);
		
		// Closer?
		if (ThisSqr < BestSqr)
		{
			Best = i;
			BestSqr = ThisSqr;
		}
	}
	
	/* Fail */
	return Best;
}

/* main() -- Main entry point */
int main(int argc, char** argv)
{
#define BUFSIZE 512
	char Buf[BUFSIZE];
	char Arg[3][BUFSIZE];
	char* p;
	char* Tok;
	size_t i, j, n;
	FILE* InTXT;
	FILE* OutWAD;
	LumpType_t Type = 0;
	
	/* Check */
	if (argc < 3)
	{
		fprintf(stderr, "Usage: %s <wadinfo.txt> <output.wad>\n", argv[0]);
		return EXIT_FAILURE;
	}
	
	/* Open inputs and outputs */
	InTXT = fopen(argv[1], "rt");
	OutWAD = fopen(argv[2], "wt");
	
	// Check
	if (!InTXT || !OutWAD)
	{
		if (InTXT)
			fclose(InTXT);
		if (OutWAD)
			fclose(OutWAD);
		return EXIT_FAILURE;
	}
	
	/* Read inputs */
	while (!feof(InTXT))
	{
		// Read line
		fgets(Buf, BUFSIZE, InTXT);
		p = Buf;
		
		// Strip any useless whitespace
		while (*p && isspace(*p))
			p++;
		
		for (n = strlen(p) - 1; isspace(p[n]) && n >= 0 && n != (size_t)-1; n--)
			p[n] = '\0';
		
		// Translate all tabs to spaces
		for (i = 0, n = strlen(p); i < n; i++)
			if (p[i] == '\t')
				p[i] = ' ';
		
		// Comment? Blank Lines? Ignore
		if (*p == '#' || !strlen(p))
			continue;
		
		// A new type group
		if (*p == '[')
		{
			p++;
			p[strlen(p) - 1] = '\0';
			
			for (i = 0; i < strlen(p); i++)
				p[i] = tolower(p[i]);
			
			fprintf(stderr, "* Group: %s\n", p);
			
			if (strcmp("lumps", p) == 0)
				Type = WT_LUMP;
			else if (strcmp("graphics", p) == 0)
				Type = WT_GRAPHIC;
			else if (strcmp("sprites", p) == 0)
				Type = WT_SPRITE;
			else if (strcmp("sounds", p) == 0)
				Type = WT_SOUND;
			else if (strcmp("flats", p) == 0)
				Type = WT_FLAT;
			else if (strcmp("rawpics", p) == 0)
				Type = WT_RAWPIC;
			else if (strcmp("picts", p) == 0)
				Type = WT_PICT;
			else
				Type = WT_LUMP;
		}
		
		// A normal lump
		else
		{
			// Clear arguments
			memset(Arg, 0, sizeof(Arg));
			
			// Get arguments for lump
			Tok = strtok(p, " ");
			
			if (Tok)
			{
				strncpy(Arg[0], Tok, BUFSIZE);
				
				Tok = strtok(NULL, " ");
				
				if (Tok)
				{
					strncpy(Arg[1], Tok, BUFSIZE);
					
					Tok = strtok(NULL, " ");
					
					if (Tok)
						strncpy(Arg[2], Tok, BUFSIZE);
				}
			}
			
			fprintf(stderr, "-> %i \"%s\" \"%s\" \"%s\"\n", Type, Arg[0], Arg[1], Arg[2]);
		}
	}
	
	/* Close */
	fclose(InTXT);
	fclose(OutWAD);
	
	/* Success! */
	return EXIT_SUCCESS;
#undef BUFSIZE
}

