package org.remood.remood.core.profile.automap;

/**
 * The automap colors which are available.
 *
 * @since 2016/05/11
 */
public enum AutomapColors
{
	/** Background Color. */
	BACKGROUND,
	
	/** Your Color. */
	YOUR_PLAYER,
	
	/** Thing Color. */
	THING,
	
	/** Ally Thing Color. */
	ALLY_THING,
	
	/** Enemy Thing Color. */
	ENEMY_THING,
	
	/** Pickup Item. */
	PICKUP,
	
	/** Wall. */
	SOLID_WALL,
	
	/** Floor Step. */
	FLOOR_STEP,
	
	/** Ceiling Step. */
	CEIL_STEP,
	
	/** Trigger line. */
	TRIGGER,
	
	/** Unmapped line. */
	UNMAPPED,
	
	/** Blockmap grid color. */
	GRID,
	
	/** Default Color. */
	DEFAULT,
	
	/** Invisible double wall. */
	INVISIBLE_WALL,
	
	/** End. */
	;
}

